class FundHistoryCrawler(object):
    def __init__(self, max_requests_per_second=5, max_threads=5):
        self.pool = PooledDB(
            creator=pymysql,
            maxconnections=12,
            mincached=2,
            maxcached=5,
            maxshared=5,
            blocking=True,
            host='localhost',
            port=3306,
            database='financedb',
            user='root',
            password='mysql'
        )
        self.session = requests.Session()
        adapter = HTTPAdapter(pool_connections=100, pool_maxsize=100)
        self.session.mount('https://', adapter)
        self.lock = threading.Lock()
        self.max_requests_per_second = max_requests_per_second
        self.semaphore = threading.Semaphore(max_requests_per_second)
        self.max_threads = max_threads
        self.progress_bars = []

    def _get_connection(self):
        return self.pool.connection()

    def _reset_auto_increment(self):
        sql_str = "ALTER TABLE fund_data AUTO_INCREMENT = 1;"
        with self.lock:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute(sql_str)

    def init_database(self, fund_code):
        self._delete_existing_data(fund_code)
        self._reset_auto_increment()
        with self._get_connection() as conn:
            conn.commit()

    def _delete_existing_data(self, fund_code):
        with self.lock:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute("DELETE FROM fund_data WHERE fund_code = %s;", (fund_code,))

    def _get_unclawed_fund(self) -> set[str]:
        with self.lock:
            with self._get_connection() as conn:
                df = pd.read_sql("SELECT fund_code FROM fund_basic WHERE history_data_statement = 0;", conn)
        return set(df['fund_code'])

    def _select_unclawed_fund(self, list_fund_code: list[str]) -> list[str]:
        unclawed_fund: set = self._get_unclawed_fund()
        result = []
        for fund_code in tqdm(list_fund_code):
            if fund_code in unclawed_fund:
                result.append(fund_code)
        print(f"去除已爬取的基金数{len(list_fund_code) - len(result)}")
        return result

    def run(self, fund_codes: list[str] | str, batches: int = None, batch_size: int = None) -> list[dict]:
        if isinstance(fund_codes, str):
            fund_codes = [fund_codes]

        selected_codes = self._select_unclawed_fund(fund_codes)
        with tqdm(total=len(selected_codes), desc="Total Progress", file=sys.stdout) as pbar:
            if batches:
                splited_codes, batch_size = self.split_list_into_batches(selected_codes, num_batches=batches)
                print(f"batches: {len(splited_codes)}, batch_size: {batch_size}")
            elif batch_size:
                splited_codes, batches = self.split_list_into_batches(selected_codes, batch_size=batch_size)
                print(f"batch_size: {batch_size}, batches: {batches}")
            else:
                random.shuffle(selected_codes)
                splited_codes = [selected_codes]

            def update_progress(future):
                pbar.update(1)

            results = []
            for batch in tqdm(splited_codes, desc="Processing batches", file=sys.stdout):

                with ThreadPoolExecutor(max_workers=self.max_threads) as executor:
                    futures = [executor.submit(self._crawl_single_fund, fund_code) for fund_code in batch]
                    for future in futures:
                        future.add_done_callback(update_progress)
                    for future in as_completed(futures):
                        results.append(future.result())

            return results

    def _mark_fund_as_crawled(self, fund_code):
        with self.lock:
            with self._get_connection() as conn:
                with conn.cursor() as cursor:
                    cursor.execute("UPDATE fund_basic SET history_data_statement = 1 WHERE fund_code = %s;",
                                   (fund_code,))
                    conn.commit()

    def _crawl_single_fund(self, fund_code, page_range: range = None) -> dict:
        try:
            total_count = self._fetch_total_count(fund_code)
            page_total = (total_count + 19) // 20
            logging.info(f"基金代码 {fund_code} 的总页数为 {page_total}")

            if page_range is None:
                page_range = range(1, page_total + 1)

            df_tmp = pd.DataFrame()

            for single_page in page_range:
                logging.info(f"基金代码 {fund_code} 正在处理第 {single_page} 页数据")
                page_data = self._fetch_page_data(single_page, fund_code)
                df_tmp = pd.concat([df_tmp, page_data], ignore_index=True)
                sleep(1 / self.max_requests_per_second)

            if df_tmp.empty:
                logging.info(f"基金代码 {fund_code} 无数据")
                self._mark_fund_as_crawled(fund_code)
                return {"fund_code": fund_code, "message": "empty", "status": 200}

            df_tmp = self._merge_info(df_tmp, fund_code)
            df_tmp = self._clean_data(df_tmp)
            self.init_database(fund_code)
            self._insert_data(df_tmp)

            self._mark_fund_as_crawled(fund_code)
            logging.info(f"==========================基金代码 {fund_code} 数据爬取完成==========================")
            return {"fund_code": fund_code, "message": "ok", "status": 200}
        except Exception as e:
            logging.error(f"Error with fund_code {fund_code}: {e}")
            logging.error(traceback.format_exc())
            return {"fund_code": fund_code, "message": "error", "status": 400}

    def _merge_info(self, df_data: pd.DataFrame, fund_code: str) -> pd.DataFrame:
        df_data["fund_code"] = fund_code
        df = df_data[["fund_code", "FSRQ", "DWJZ", "LJJZ", "JZZZL"]]
        return df

    def _clean_data(self, df: pd.DataFrame) -> pd.DataFrame:
        df['DWJZ'] = pd.to_numeric(df['DWJZ'], errors='coerce')
        df['LJJZ'] = pd.to_numeric(df['LJJZ'], errors='coerce')
        df['JZZZL'] = pd.to_numeric(df['JZZZL'], errors='coerce')
        df = df.fillna(0)
        return df

    def _fetch_total_count(self, fund_code):
        with self.semaphore:
            url = f"https://api.fund.eastmoney.com/f10/lsjz"

            params = {
                "callback": "jQuery18304038998523093684_1586160530315",
                "fundCode": fund_code,
                "pageIndex": 1,
                "pageSize": 20
            }
            header = {"Referer": f"https://fundf10.eastmoney.com/jjjz_{fund_code}.html",
                      "User-Agent": ua.random}
            response = self.session.get(url, headers=header, params=params)

            response.raise_for_status()

            json_data = json.loads(response.text[41:-1])
            logging.info(f"基金代码 {fund_code} 初次执行结果：\n{json_data}")
            return json_data.get("TotalCount")

    def _fetch_page_data(self, page, fund_code) -> pd.DataFrame:
        with self.semaphore:
            url = f"https://api.fund.eastmoney.com/f10/lsjz"
            params = {
                "callback": "jQuery18304038998523093684_1586160530315",
                "fundCode": fund_code,
                "pageIndex": page,
                "pageSize": 20
            }
            header = {"Referer": f"https://fundf10.eastmoney.com/jjjz_{fund_code}.html",
                      "User-Agent": ua.random}
            response = self.session.get(url, headers=header, params=params)
            response.raise_for_status()

            json_data = json.loads(response.text[41:-1])
            list_date_data = json_data.get("Data", {"LSJZList": None}).get("LSJZList")
            df = pd.DataFrame(list_date_data)

            return df

    def _insert_data(self, df_data: pd.DataFrame):
        sql = "INSERT INTO fund_data(fund_code, date, nav, c_nav, growth_rate) VALUES(%s, %s, %s, %s, %s);"
        data = df_data.values.tolist()
        try:
            with self.lock:
                with self._get_connection() as conn:
                    with conn.cursor() as cur:
                        cur.executemany(sql, data)
                        conn.commit()
        except Error as e:
            logging.error(f"Error inserting data: {e}")
            for row in data:
                try:
                    cur.execute(sql, row)
                except Error as row_error:
                    logging.error(f"Error inserting row: {row} with error: {row_error}")

    def __del__(self):
        try:
            self.pool.close()
        except Exception as e:
            logging.error(f"Error closing connection pool: {e}")

    @staticmethod
    def split_list_into_batches(lst, num_batches=None, batch_size=None) -> tuple[list[list], int]:
        if num_batches is not None and batch_size is not None:
            raise ValueError("Only one of num_batches or batch_size should be specified.")

        if num_batches is None and batch_size is None:
            raise ValueError("Either num_batches or batch_size must be specified.")

        if num_batches is not None:
            avg_batch_size = len(lst) // num_batches
            remainder = len(lst) % num_batches

            batches = []
            start = 0

            for i in range(num_batches):
                end = start + avg_batch_size + (1 if i < remainder else 0)
                batches.append(lst[start:end])
                start = end

            return batches, avg_batch_size

        if batch_size is not None:
            batches = [lst[i:i + batch_size] for i in range(0, len(lst), batch_size)]
            return batches, len(batches[0])


